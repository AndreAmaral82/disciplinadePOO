    private void carregarTabela(){
        String[] colunas = {"Nome do Paciente", "Nome do Médico", "Urgência"};
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.setColumnIdentifiers(colunas);
        
        for(Consulta cons : listaDeConsulta){
           Object[] linha = {cons.nomePaciente, cons.nomeMedico, cons.urgencia};
           tableModel.addRow(linha);
        }
        
        tableConsultas.setModel(tableModel);
    }

