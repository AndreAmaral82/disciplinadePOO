    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    String nomePaciente = txtNomePaciente.getText();
    String nomeMedico = txtNomeMedico.getText();
    if(nomePaciente.isEmpty() || nomeMedico.isEmpty() || 
            cmbUrgencia.getSelectedIndex()==0){
    JOptionPane.showMessageDialog(null, "Todos dados precisam ser preenchidos!");
      
    }else{
        Consulta consul = new Consulta();
        consul.nomePaciente = nomePaciente;
        consul.nomeMedico = nomeMedico;
        consul.urgencia = cmbUrgencia.getSelectedItem().toString();
        listaDeConsulta.add(consul);
        
        carregarTabela();
        
        txtNomePaciente.setText("");
        txtNomeMedico.setText("");
        cmbUrgencia.setSelectedIndex(0);
    }
            
    }//GEN-LAST:event_btnSalvarActionPerformed


